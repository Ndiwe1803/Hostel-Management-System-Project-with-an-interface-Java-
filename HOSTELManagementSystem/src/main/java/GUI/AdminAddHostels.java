    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String roomID = jTextField1.getText();
        String capacityStr = jTextField2.getText();
        String priceStr = jTextField5.getText();
        String balcony = jComboBox1.getSelectedItem().toString();

        // Validation: Check if roomID is duplicate
        if (isRoomIDDuplicate(roomID)) {
            JOptionPane.showMessageDialog(this, "Room ID already exists. Please enter a unique Room ID.");
            return;
        }

        // Validation: Check if capacity is a number between 1 and 6
        int capacity;
        try {
            capacity = Integer.parseInt(capacityStr);
            if (capacity < 1 || capacity > 6) {
                throw new NumberFormatException();
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Capacity should be a number between 1 and 6.");
            return;
        }

        // Validation: Check if price is a number
        double price;
        try {
            price = Integer.parseInt(priceStr);
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Price should be a number.");
            return;
        }

        // Call the addHostelRoom() method with validated data
        Admin admin = new Admin(); // Assuming an instance of the Admin class is created
        admin.addHostelRoom(roomID, String.valueOf(capacity), String.valueOf(price), balcony);

        // Show success message
        JOptionPane.showMessageDialog(this, "Hostel room added successfully!");        // TODO add your handling code here:
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String roomID = jTextField1.getText();
        String capacityStr = jTextField2.getText();
        String priceStr = jTextField5.getText();
        String balcony = jComboBox1.getSelectedItem().toString();

        // Validation: Check if roomID exists
        if (!isRoomIDExistent(roomID)) {
            JOptionPane.showMessageDialog(this, "Room ID does not exist. Please enter a valid Room ID.");
            return;
        }

        // Validation: Check if capacity is a number between 1 and 6
        int capacity;
        try {
            capacity = Integer.parseInt(capacityStr);
            if (capacity < 1 || capacity > 6) {
                throw new NumberFormatException();
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Capacity should be a number between 1 and 6.");
            return;
        }

        // Validation: Check if price is a number
        double price;
        try {
            price = Integer.parseInt(priceStr);
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Price should be a number.");
            return;
        }

        // Update the hostel room record with validated data
        Admin a = new Admin();
        a.updateHostelRoom(roomID, String.valueOf(capacity), String.valueOf(price), balcony);

        // Show success message
        JOptionPane.showMessageDialog(this, "Hostel room updated successfully!");
    }//GEN-LAST:event_jButton2ActionPerformed

