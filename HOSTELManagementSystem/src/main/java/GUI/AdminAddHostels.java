    private boolean isRoomIDDuplicate(String roomID) {
        String filePath = "C:\\Users\\G5\\Documents\\NetBeansProjects\\HOSTELManagementSystem\\src\\main\\java\\Databases\\hostels.txt";

        try (BufferedReader br = new BufferedReader(new FileReader(filePath))) {
            String line;
            while ((line = br.readLine()) != null) {
                String[] roomInfo = line.split(",");
                String storedRoomID = roomInfo[0];

                if (roomID.equals(storedRoomID)) {
                    return true; // Duplicate roomID found
                }
            }
        } catch (IOException e) {
            e.printStackTrace();
        }

        return false; // RoomID is unique
    }
    
    // Helper method to check if a roomID exists in the hostels.txt file
    private boolean isRoomIDExistent(String roomID) {
        String filePath = "C:\\Users\\G5\\Documents\\NetBeansProjects\\HOSTELManagementSystem\\src\\main\\java\\Databases\\hostels.txt";

        try (BufferedReader br = new BufferedReader(new FileReader(filePath))) {
            String line;
            while ((line = br.readLine()) != null) {
                String[] roomInfo = line.split(",");
                String storedRoomID = roomInfo[0];

                if (roomID.equals(storedRoomID)) {
                    return true; // RoomID exists
                }
            }
        } catch (IOException e) {
            e.printStackTrace();
        }

        return false; // RoomID does not exist
    }
    
